# Definição Variavis:
a = float(input('Defina a variavel A; '))
b = float(input('Defina a variavel B; '))
c = float(input('Defina a variavel C; '))
pi = 3.14159

# Definição Codigos:
print('Triângulo;', (a*c)/2)
print('Círcilo;', pi*c*c)
print('Trapézio;', ((a+b)*c)/2)
print('Quadrado;', b**2)
print('Retângulo;', a*b)
print('Triângulo Retângulo;', (a*c)/2)